export function searchEmojis(query: string): Array<{ emoji: string; name: string }> {
  const allEmojis = Object.values(emojiCategories).flat();
  
  if (!query.trim()) {
    return popularEmojis.map(emoji => {
      const found = allEmojis.find(e => e.emoji === emoji);
      return found || { emoji, name: emoji };
    });
  }

  return allEmojis.filter(item => 
    item.name.toLowerCase().includes(query.toLowerCase()) ||
    item.emoji.includes(query)
  );
}

export function getEmojiByName(name: string): string | null {
  const allEmojis = Object.values(emojiCategories).flat();
  const found = allEmojis.find(item => item.name === name);
  return found ? found.emoji : null;
}

export function getRandomEmoji(): string {
  const allEmojis = Object.values(emojiCategories).flat();
  const randomIndex = Math.floor(Math.random() * allEmojis.length);
  return allEmojis[randomIndex].emoji;
}
